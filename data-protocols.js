// Detect needed protocol from recent user messages
export const detectProtocolNeed = (recentMessages) => {
  if (!recentMessages || recentMessages.length === 0) return null;

  const text = recentMessages.join(' ').toLowerCase();
  const scores = {};

  for (const [pattern, config] of Object.entries(PROTOCOL_PATTERNS)) {
    const matches = config.keywords.filter(kw => text.includes(kw)).length;
    if (matches >= 2) scores[pattern] = matches;
  }

  if (Object.keys(scores).length === 0) return null;

  const top = Object.entries(scores).sort((a, b) => b[1] - a[1])[0];
  return PROTOCOL_PATTERNS[top[0]].protocol;
};
